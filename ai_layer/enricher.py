def _build_user_message(
    record: Union[ConditionRecord, MedicationRecord],
    patient_context: dict | None = None,
) -> str:
    """Format a staging record into the user turn, optionally with patient context."""
    if isinstance(record, ConditionRecord):
        lines = [
            "Record type: CONDITION (code system: SNOMED CT)",
            f"Patient ID: {record.patient_id}",
            f"Code: {record.condition_code}",
            f"Description: {record.condition_description}",
            f"Onset date: {record.onset_date or 'unknown'}",
        ]
    else:
        lines = [
            "Record type: MEDICATION (code system: RxNorm)",
            f"Patient ID: {record.patient_id}",
            f"Code: {record.medication_code}",
            f"Description: {record.medication_description}",
            f"Start date: {record.start_date or 'unknown'}",
        ]

    if patient_context:
        other_conditions = patient_context.get("conditions", [])
        other_medications = patient_context.get("medications", [])
        if other_conditions or other_medications:
            lines.append("\nPatient context (co-occurring records for this patient):")
        if other_conditions:
            lines.append("  Other conditions:")
            for c in other_conditions[:5]:
                lines.append(f"    - {c['code']}: {c['description']}")
        if other_medications:
            lines.append("  Other medications:")
            for m in other_medications[:5]:
                lines.append(f"    - {m['code']}: {m['description']}")

    lines.append("\nScore this record across all 6 clinical categories.")
    return "\n".join(lines)


# Sonnet pricing per token (USD)
_INPUT_COST = 3.0 / 1_000_000
_OUTPUT_COST = 15.0 / 1_000_000
_CACHE_READ_COST = 0.30 / 1_000_000
_CACHE_WRITE_COST = 3.75 / 1_000_000


def enrich_record(
    record: Union[ConditionRecord, MedicationRecord],
    patient_context: dict | None = None,
) -> tuple[EnrichmentResult, dict]:
    """Call Claude to enrich a single record. Returns (EnrichmentResult, usage_dict).

    Uses prompt caching on the system prompt (static clinical context).
    Raises on tool_use failure.
    """
    response = _client.messages.create(
        model=MODEL,
        max_tokens=1024,
        system=[
            {
                "type": "text",
                "text": SYSTEM_PROMPT,
                "cache_control": {"type": "ephemeral"},
            }
        ],
        tools=[ENRICHMENT_TOOL],
        tool_choice={"type": "tool", "name": "submit_enrichment"},
        messages=[
            {"role": "user", "content": _build_user_message(record, patient_context)},
        ],
    )

    tool_block = next(
        (b for b in response.content if b.type == "tool_use"), None
    )
    if tool_block is None:
        raise RuntimeError(
            f"Claude did not return a tool_use block for {record}. "
            f"Stop reason: {response.stop_reason}"
        )

    raw: dict = tool_block.input
    u = response.usage
    input_tokens = u.input_tokens
    output_tokens = u.output_tokens
    cache_read = getattr(u, "cache_read_input_tokens", 0) or 0
    cache_write = getattr(u, "cache_creation_input_tokens", 0) or 0
    cost_usd = (
        input_tokens * _INPUT_COST
        + output_tokens * _OUTPUT_COST
        + cache_read * _CACHE_READ_COST
        + cache_write * _CACHE_WRITE_COST
    )
    usage = {
        "input_tokens": input_tokens,
        "output_tokens": output_tokens,
        "cache_read_tokens": cache_read,
        "cache_write_tokens": cache_write,
        "cost_usd": cost_usd,
    }

    record_code = (
        record.condition_code
        if isinstance(record, ConditionRecord)
        else record.medication_code
    )
    record_description = (
        record.condition_description
        if isinstance(record, ConditionRecord)
        else record.medication_description
    )
    record_type = "condition" if isinstance(record, ConditionRecord) else "medication"

    result = EnrichmentResult(
        patient_id=record.patient_id,
        record_type=record_type,
        record_code=record_code,
        record_description=record_description,
        diagnosis_specificity=CategoryScore(**raw["diagnosis_specificity"]),
        clinical_urgency=CategoryScore(**raw["clinical_urgency"]),
        coding_accuracy=CategoryScore(**raw["coding_accuracy"]),
        medication_appropriateness=CategoryScore(**raw["medication_appropriateness"]),
        drug_condition_alignment=CategoryScore(**raw["drug_condition_alignment"]),
        comorbidity_risk=CategoryScore(**raw["comorbidity_risk"]),
        overall_confidence=raw["overall_confidence"],
    )
    return result, usage


def enrich_batch(
    records: list[Union[ConditionRecord, MedicationRecord]],
    *,
    patient_context: dict | None = None,
    stop_on_error: bool = False,
    max_workers: int = 10,
) -> tuple[list[EnrichmentResult], list[dict], dict]:
    """Enrich a list of records concurrently. Returns (results, errors, usage).

    patient_context: optional dict[patient_id -> {conditions, medications}]
    usage: aggregated token counts and cost_usd across all calls.
    """
    results: list[EnrichmentResult] = []
    errors: list[dict] = []
    agg = {"input_tokens": 0, "output_tokens": 0, "cache_read_tokens": 0, "cache_write_tokens": 0, "cost_usd": 0.0}

    with ThreadPoolExecutor(max_workers=max_workers) as executor:
        future_to_record = {
            executor.submit(
                enrich_record,
                r,
                patient_context.get(r.patient_id) if patient_context else None,
            ): r
            for r in records
        }
        for future in as_completed(future_to_record):
            record = future_to_record[future]
            try:
                result, usage = future.result()
                results.append(result)
                for k in agg:
                    agg[k] += usage[k]
            except Exception as exc:
                err = {
                    "patient_id": record.patient_id,
                    "record_code": getattr(record, "condition_code", None)
                    or getattr(record, "medication_code", None),
                    "error": str(exc),
                }
                errors.append(err)
                if stop_on_error:
                    raise

    return results, errors, agg
